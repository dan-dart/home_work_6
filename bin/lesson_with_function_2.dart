//Создайте функцию isEven, которая принимает один параметр: number (целое число).
//Функция isEven должна проверить, является ли переданное число четным.
//Если число четное, функция должна вернуть true, в противном случае false.

//В основной функции программы вызовите функцию isEven и передайте ей целое
