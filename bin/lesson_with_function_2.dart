//Пример вывода:
//Число 6 четное: true

isEven(number) {
  bool y;
  if (number % 2 == 0) {
    y = true;
  } else {
    y = false;
  }
  return y;
}

//Напишите функцию calculateArea, которая принимает два аргумента:
//width (ширина) и height (высота) прямоугольника, и возвращает его площадь.
//Затем вызовите эту функцию с различными значениями ширины и высоты,
//чтобы убедиться, что она правильно работает.

calculateArea(width, height) {
  int area;
  return area = width * height;
}

